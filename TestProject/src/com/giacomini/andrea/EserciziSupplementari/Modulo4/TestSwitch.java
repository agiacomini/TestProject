public class TestSwitch {

	public static void main(String[] args){
		
//		byte, short, int, long ,enum, String, Byte, Short, Integer, Long, char, Character
		
		int test = 1;
		
		switch(test){
		
		case 1: System.out.println("var test 1"); break; 
			
		case 2: System.out.println("var test 2"); break;
			
		case 3: System.out.println("var test 3"); break;
			
		case 4: System.out.println("var test 4"); break;
			
		case 5: System.out.println("var test 5"); break;
		
		default: System.out.println("var test default"); break;
		
		}
		
	}
}
